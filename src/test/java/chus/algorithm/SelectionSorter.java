package chus.algorithm;

public class SelectionSorter<T extends Comparable<T>> extends Sorter<T> {
    @Override
    public void sort(T[] a) {
        int len = a.length;
        for (int i = 0; i < len; i++) {
            int min = i;
            for (int j = i + 1; j < len; j++) {
                if (less(a[j], a[min])) {
                    min = j;
                }
            }
            exch(a, i, min);
        }
    }
}
